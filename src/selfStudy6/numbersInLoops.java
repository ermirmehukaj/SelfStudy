package selfStudy6;

public class numbersInLoops {

    public static void main(String[] args) {

        for (int i = 0; i < 101; i++) {
            if (i % 3 == 0) {
                System.out.print(i + " ,");

            }}
        for (int i = 0; i < 101; i++) {


            if (i % 5 == 0) {
                System.out.print("\n" + i +", ");

            }}

    }
}
